create table if not exists public.settings (
  id int primary key,
  phone_display text not null default '',
  phone_e164 text not null default '',
  email text not null default '',
  address text not null default '',
  whatsapp_message text not null default '',
  hero_image text not null default '',
  about_image text not null default ''
);

create table if not exists public.cars (
  id bigint generated by default as identity primary key,
  name text not null,
  model text not null default '',
  seats int not null default 4,
  bags text not null default '2',
  rate int not null default 12,
  tag text not null default '',
  description text not null default '',
  image_url text not null default '',
  sort int not null default 99
);

create table if not exists public.routes (
  id bigint generated by default as identity primary key,
  title text not null,
  meta text not null default '',
  price text not null default '',
  image_url text not null default '',
  sort int not null default 99
);

create table if not exists public.testimonials (
  id bigint generated by default as identity primary key,
  name text not null,
  meta text not null default '',
  stars int not null default 5,
  quote text not null default '',
  sort int not null default 99
);

create table if not exists public.gallery (
  id bigint generated by default as identity primary key,
  caption text not null default '',
  image_url text not null default '',
  span text not null default '',
  sort int not null default 99
);

create table if not exists public.enquiries (
  id bigint generated by default as identity primary key,
  name text not null,
  phone text not null,
  from_city text not null default '',
  to_city text not null default '',
  message text not null default '',
  status text not null default 'new',
  created_at timestamptz not null default now()
);

-- RLS: public can read site content; nobody (except service role) can write.
-- Enquiries are write-only for the public; only service role reads them.
alter table public.settings enable row level security;
alter table public.cars enable row level security;
alter table public.routes enable row level security;
alter table public.testimonials enable row level security;
alter table public.gallery enable row level security;
alter table public.enquiries enable row level security;

drop policy if exists "public read settings" on public.settings;
create policy "public read settings" on public.settings for select using (true);
drop policy if exists "public read cars" on public.cars;
create policy "public read cars" on public.cars for select using (true);
drop policy if exists "public read routes" on public.routes;
create policy "public read routes" on public.routes for select using (true);
drop policy if exists "public read testimonials" on public.testimonials;
create policy "public read testimonials" on public.testimonials for select using (true);
drop policy if exists "public read gallery" on public.gallery;
create policy "public read gallery" on public.gallery for select using (true);
